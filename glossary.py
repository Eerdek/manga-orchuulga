# ---------------------------------------------------------
# KEEP — яг хэвээр үлдээнэ (орд/бичлэгийг устгахгүй, орчуулахгүй)
# Доорх нь түгээмэл SFX, тэмдэг, богино кодууд.
# ---------------------------------------------------------
KEEP_AS_IS = {
    "SFX", "WHISPER", "BAM", "WHOOSH", "CLICK", "BEEP",
    "TV", "OK", "NG",
    "--", "—", "–", "...", "…",
    "!!", "?!", "!?", "???",
}

# ---------------------------------------------------------
# KEEP_PATTERNS — regex хэлбэрийн хэв шинжүүдийг мөн хэвээр үлдээнэ.
# Ж: бүх капслок богино код, дан тоо гэх мэт.
# ---------------------------------------------------------
KEEP_PATTERNS = [
    r"\b[0-9]{1,4}\b",           # 1–4 оронтой дан тоо (жишээ нь: 200)
    r"\b[A-Z]{2,}\b",            # CAPs үсгээрх богино кодууд (TV, SFX, etc.)
    r"(?:…|\.{3})",              # гурван цэгийн хоёр хэлбэр
    r"(?:--|—|–)",               # урт зураасны хувилбарууд
    r"[!?]{2,}",                 # давтагдсан ? ! тэмдгүүд
]

